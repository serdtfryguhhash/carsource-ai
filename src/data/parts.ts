// ==================== HELPER FUNCTIONS ====================

export function getPartBySlug(slug: string): ModPart | undefined {
  return modParts.find((p) => p.slug === slug);
}

export function getPartsByCategory(category: ModCategory): ModPart[] {
  return modParts.filter((p) => p.category === category);
}

export function getPartsForVehicle(vehicleSlug: string): ModPart[] {
  return modParts.filter((p) => p.compatible_vehicles.includes(vehicleSlug));
}

export function getPopularParts(): ModPart[] {
  return modParts.filter((p) => p.is_popular);
}
